// a sortDict func that takes an object of number pairs and returns an Sorted
// array on the objects number pairs.
function sortDict(object) {
  // if we want to first convert object key value pairs into arrays, we have to
  // take each object property and value and save those as arrays, then push that
  // to a new array, and sort that array. So:
  // our variable to stash new arrays
  var arr1 = [];
  // our variable to stash the grand array
  var arr2 = [];
  // a for loop will go through each object and set it as
  for(key in object) {
    arr1.push(object.key,key);
    arr2.push[arr1];
  }
  return arr2;
}



